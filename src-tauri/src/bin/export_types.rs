use std::fs;
use std::path::{Path, PathBuf};
use ts_rs::TS;

// Each later task (A3-A7) adds its public IPC types to this list. The build
// fails fast if a type missing the `#[derive(TS)]` attribute is named here.
fn export_all() -> Result<String, Box<dyn std::error::Error>> {
    let header = "// AUTO-GENERATED by `cargo run --bin export_types`. Do not edit by hand.\n\
                  // Source of truth lives in src-tauri/src/*.rs `#[derive(TS)]` blocks.\n\n";
    let mut buf = String::from(header);

    // A2 is the only task with an IPC-bearing type at this stage:
    buf.push_str(&mdviewer_lib::BuildInfo::export_to_string().unwrap());
    buf.push_str("\n");

    // A3 will append `Settings`, `ProfileSettings`, `AppearanceSettings`,
    //  `EditorSettings`, `CommentsSettings`, `AdvancedSettings` here.
    // A5 appends `RenderResult`, `RenderOptions`.
    // A6 appends `Anchor`, `ResolveOutcome`.
    // A7 appends `Thread`, `Comment`, `NewThread`, `NewComment`.
    // A8a appends `OpenResult`, `OpenOutcome`, `OpenOpts`.

    Ok(buf)
}

// Writes the export_all() body to `out` and reports the path on stderr.
// Extracted from `main` so unit tests can drive it against a temp directory.
fn write_to(out: &Path) -> Result<(), Box<dyn std::error::Error>> {
    let body = export_all()?;
    fs::write(out, body)?;
    eprintln!("wrote {}", out.display());
    Ok(())
}

fn main() -> Result<(), Box<dyn std::error::Error>> {
    let out: PathBuf = ["..", "src", "types-generated.ts"].iter().collect();
    write_to(&out)
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn export_all_emits_header_and_buildinfo() {
        let body = export_all().expect("export_all should succeed");
        assert!(
            body.starts_with("// AUTO-GENERATED"),
            "body must start with the AUTO-GENERATED marker"
        );
        assert!(body.contains("BuildInfo"), "body must declare BuildInfo");
        assert!(body.contains("version"), "body must include the version field");
        assert!(
            body.contains("commit_hash"),
            "body must include the commit_hash field"
        );
    }

    #[test]
    fn write_to_creates_expected_file() {
        // Exercise the file-write path that `main` runs (export_all +
        // fs::write + eprintln) against a temp directory so we never clobber
        // the real `src/types-generated.ts` from inside `cargo test`.
        let dir = tempfile::tempdir().expect("tempdir");
        let out = dir.path().join("types-generated.ts");
        write_to(&out).expect("write_to should succeed");
        let read_back = fs::read_to_string(&out).expect("read should succeed");
        assert!(read_back.starts_with("// AUTO-GENERATED"));
        assert!(read_back.contains("BuildInfo"));
    }
}
