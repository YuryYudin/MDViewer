use std::fs;
use std::path::{Path, PathBuf};
use ts_rs::TS;

// Each later task (A3-A7) adds its public IPC types to this list. The build
// fails fast if a type missing the `#[derive(TS)]` attribute is named here.
fn export_all() -> Result<String, Box<dyn std::error::Error>> {
    let header = "// AUTO-GENERATED by `cargo run --bin export_types`. Do not edit by hand.\n\
                  // Source of truth lives in src-tauri/src/*.rs `#[derive(TS)]` blocks.\n\n";
    let mut buf = String::from(header);

    // A2 is the only task with an IPC-bearing type at this stage:
    buf.push_str(&mdviewer_lib::BuildInfo::export_to_string().unwrap());
    buf.push_str("\n");

    // A3: settings types — three enums and six structs (Settings root last).
    buf.push_str(&mdviewer_lib::settings::Theme::export_to_string().unwrap());
    buf.push_str("\n");
    buf.push_str(&mdviewer_lib::settings::ExternalChangeBehavior::export_to_string().unwrap());
    buf.push_str("\n");
    buf.push_str(&mdviewer_lib::settings::AutoMergeMode::export_to_string().unwrap());
    buf.push_str("\n");
    buf.push_str(&mdviewer_lib::settings::StartupMode::export_to_string().unwrap());
    buf.push_str("\n");
    buf.push_str(&mdviewer_lib::settings::DarkVariant::export_to_string().unwrap());
    buf.push_str("\n");
    buf.push_str(&mdviewer_lib::settings::ProfileSettings::export_to_string().unwrap());
    buf.push_str("\n");
    buf.push_str(&mdviewer_lib::settings::AppearanceSettings::export_to_string().unwrap());
    buf.push_str("\n");
    buf.push_str(&mdviewer_lib::settings::EditorSettings::export_to_string().unwrap());
    buf.push_str("\n");
    buf.push_str(&mdviewer_lib::settings::CommentsSettings::export_to_string().unwrap());
    buf.push_str("\n");
    buf.push_str(&mdviewer_lib::settings::AdvancedSettings::export_to_string().unwrap());
    buf.push_str("\n");
    // A1: Drive/Cloud sub-tree types — BackendMode enum + DriveSettings +
    // CloudSettings struct, emitted before `Settings` so its `cloud` field
    // resolves the inline declaration without dangling imports.
    buf.push_str(&mdviewer_lib::settings::BackendMode::export_to_string().unwrap());
    buf.push_str("\n");
    buf.push_str(&mdviewer_lib::settings::DriveSettings::export_to_string().unwrap());
    buf.push_str("\n");
    buf.push_str(&mdviewer_lib::settings::CloudSettings::export_to_string().unwrap());
    buf.push_str("\n");
    buf.push_str(&mdviewer_lib::settings::Settings::export_to_string().unwrap());
    buf.push_str("\n");

    // A5: document render types — options input + result payload.
    buf.push_str(&mdviewer_lib::document::RenderOptions::export_to_string().unwrap());
    buf.push_str("\n");
    buf.push_str(&mdviewer_lib::document::RenderResult::export_to_string().unwrap());
    buf.push_str("\n");

    // A6: anchor model + resolve outcome (W3C TextQuote/TextPosition flattened).
    buf.push_str(&mdviewer_lib::anchor::Anchor::export_to_string().unwrap());
    buf.push_str("\n");
    buf.push_str(&mdviewer_lib::anchor::ResolveOutcome::export_to_string().unwrap());
    buf.push_str("\n");

    // A7: comments + sidecar — Comment, Thread, NewComment, NewThread.
    buf.push_str(&mdviewer_lib::comments::Comment::export_to_string().unwrap());
    buf.push_str("\n");
    buf.push_str(&mdviewer_lib::comments::Thread::export_to_string().unwrap());
    buf.push_str("\n");
    buf.push_str(&mdviewer_lib::comments::NewComment::export_to_string().unwrap());
    buf.push_str("\n");
    buf.push_str(&mdviewer_lib::comments::NewThread::export_to_string().unwrap());
    buf.push_str("\n");

    // A8a: workspace open shapes — OpenResult and the Document|Conflict union.
    buf.push_str(&mdviewer_lib::workspace::OpenResult::export_to_string().unwrap());
    buf.push_str("\n");
    buf.push_str(&mdviewer_lib::workspace::OpenOutcome::export_to_string().unwrap());
    buf.push_str("\n");
    // TabSummary — list_open_documents return shape. Emits {id, path}; the
    // frontend used to receive bare ids and surface them in tab labels.
    buf.push_str(&mdviewer_lib::workspace::TabSummary::export_to_string().unwrap());
    buf.push_str("\n");

    // C2: conflict-diff hunk types consumed by Conflict.ts.
    buf.push_str(&mdviewer_lib::conflict::HunkKind::export_to_string().unwrap());
    buf.push_str("\n");
    buf.push_str(&mdviewer_lib::conflict::Hunk::export_to_string().unwrap());
    buf.push_str("\n");

    // C3: export result returned by the share/export IPC command.
    buf.push_str(&mdviewer_lib::workspace::ExportResult::export_to_string().unwrap());
    buf.push_str("\n");

    // RecentEntry — list_recents IPC return shape (path + mtime).
    buf.push_str(&mdviewer_lib::recents::RecentEntry::export_to_string().unwrap());
    buf.push_str("\n");

    // Font-size feature: per-document font-size override. Crosses IPC via
    // `get_doc_pref` / `set_doc_pref` / `delete_doc_pref` (see main.rs).
    buf.push_str(&mdviewer_lib::doc_prefs::DocPref::export_to_string().unwrap());
    buf.push_str("\n");

    // A5 appends `RenderResult`, `RenderOptions`.
    // A6 appends `Anchor`, `ResolveOutcome`.
    // A7 appends `Thread`, `Comment`, `NewThread`, `NewComment`.
    // A8a appends `OpenResult`, `OpenOutcome`.
    // C2 appends `Hunk`, `HunkKind`.

    Ok(buf)
}

// Writes the export_all() body to `out` and reports the path on stderr.
// Extracted from `main` so unit tests can drive it against a temp directory.
fn write_to(out: &Path) -> Result<(), Box<dyn std::error::Error>> {
    let body = export_all()?;
    fs::write(out, body)?;
    eprintln!("wrote {}", out.display());
    Ok(())
}

fn main() -> Result<(), Box<dyn std::error::Error>> {
    let out: PathBuf = ["..", "src", "types-generated.ts"].iter().collect();
    write_to(&out)
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn export_all_emits_header_and_buildinfo() {
        let body = export_all().expect("export_all should succeed");
        assert!(
            body.starts_with("// AUTO-GENERATED"),
            "body must start with the AUTO-GENERATED marker"
        );
        assert!(body.contains("BuildInfo"), "body must declare BuildInfo");
        assert!(body.contains("version"), "body must include the version field");
        assert!(
            body.contains("commit_hash"),
            "body must include the commit_hash field"
        );
    }

    #[test]
    fn write_to_creates_expected_file() {
        // Exercise the file-write path that `main` runs (export_all +
        // fs::write + eprintln) against a temp directory so we never clobber
        // the real `src/types-generated.ts` from inside `cargo test`.
        let dir = tempfile::tempdir().expect("tempdir");
        let out = dir.path().join("types-generated.ts");
        write_to(&out).expect("write_to should succeed");
        let read_back = fs::read_to_string(&out).expect("read should succeed");
        assert!(read_back.starts_with("// AUTO-GENERATED"));
        assert!(read_back.contains("BuildInfo"));
    }
}
